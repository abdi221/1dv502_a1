/**
 * Album class.
 */
public class Album {
  private String name;
  private int releaseYear;
  private String artist;

  /**
   * Constructor for album class
   */
  public Album(String name, int releaseYear, String artist) {
    this.name = name;
    this.releaseYear = releaseYear;
    this.artist = artist;
  }

}